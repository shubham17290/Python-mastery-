#  Q 01: Check whether a number is positive, or  negative or invalid !
num = float(input("Enter your number sir/ ma'am  : "))

if num > 0:
    print(f"number : {num} is positive !")
elif num < 0:
    print(f"number :{num} is negative !")
elif num == 0:
    print(" you have entered zero ")
else:
    print(f"you have entered the invalid number/value {num}")


   
