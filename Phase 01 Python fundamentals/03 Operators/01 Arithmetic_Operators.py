a = 10.76
b = 3.5

print(a + b)  # 14.26 : Addition operator
print(a - b)  # 7.26 :subtraction operator
print(a * b)  # 37.66  : multiplication operator
print(a / b)  # 3.07428 : division operator
print(a // b)  # 3.0 : floor division operator
print(a % b)  #   : this is the modulo  operator  # will return the remainder
# modulo calculation formula : a%b=a−(b×(a//b))
print(a**b)  # 1000 : this is the power operator

# type conversion inside the print function
a = 34
print(a // 3)  # it will return the integer return value

c = 37
d = 5
print(c % d)

